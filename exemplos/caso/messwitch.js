let mesdoano = parseInt(prompt("Digite um número para saber o mês do ano: "));

switch (mesdoano) {
    case 1:
        alert("janeiro")
        break
    case 2:
        alert("Fevereiro")
        break
    case 3:
        alert("Março")
        break
    case 4:
        alert("Abril")
        break
    case 5:
        alert("Maio")
        break
    case 6:
        alert("junho")
        break
    case 7:
        alert("julho")
        break
    case 8:
        alert("Agosto")
        break
    case 9:
        alert("Setembro")
        break
    case 10:
        alert("Outubro")
        break
    case 11:
        alert("Novembro")
        break
    case 12:
        alert("Desembro")
        break
    default:
        alert("Mês Inválido!")
}